package com.hsbc.inmediauto.entidad;

public class InformedParams {
	private String catalogo;
	private String cveVeh;
	private String cveServicio;
	private String cveCarga;
	private String cveCategoria;
	private String cveUso;
	private String cveClasificacionVeh;
	private String esquemaPago;

	public String getCatalogo() {
		return catalogo;
	}

	public void setCatalogo(String catalogo) {
		this.catalogo = catalogo;
	}

	public String getCveVeh() {
		return cveVeh;
	}

	public void setCveVeh(String cveVeh) {
		this.cveVeh = cveVeh;
	}

	public String getCveServicio() {
		return cveServicio;
	}

	public void setCveServicio(String cveServicio) {
		this.cveServicio = cveServicio;
	}

	public String getCveCarga() {
		return cveCarga;
	}

	public void setCveCarga(String cveCarga) {
		this.cveCarga = cveCarga;
	}

	public String getCveCategoria() {
		return cveCategoria;
	}

	public void setCveCategoria(String cveCategoria) {
		this.cveCategoria = cveCategoria;
	}

	public String getCveUso() {
		return cveUso;
	}

	public void setCveUso(String cveUso) {
		this.cveUso = cveUso;
	}

	public String getCveClasificacionVeh() {
		return cveClasificacionVeh;
	}

	public void setCveClasificacionVeh(String cveClasificacionVeh) {
		this.cveClasificacionVeh = cveClasificacionVeh;
	}

	public String getEsquemaPago() {
		return esquemaPago;
	}

	public void setEsquemaPago(String esquemaPago) {
		this.esquemaPago = esquemaPago;
	}


}
